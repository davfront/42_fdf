#include "fdf.h"

t_trgb	fdf_color_trgb(int color)
{
	t_trgb	c;

	c.b = color % 256;
	c.g = (color / 256) % 256;
	c.r = (color / 256 / 256) % 256;
	c.t = color / 256 / 256 / 256;
	return (c);
}
